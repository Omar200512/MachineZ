     private void configurarTabla() {
        // Configurar modelo de tabla
        DefaultTableModel modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("#Pedido");
        modeloTabla.addColumn("Producto");
        modeloTabla.addColumn("Apellidos del Cliente");
        modeloTabla.addColumn("Consultar Pedido");
        jTablePedidos.setModel(modeloTabla);
    }
   
   private void cargarDatosTabla() {
    // Aqu√≠ debes obtener los datos de los pedidos desde la base de datos
    // y luego agregar cada pedido a la tabla
    Pedido pedido = new Pedido(0, null, null); // Instancia de Pedido para obtener los pedidos
    List<Document> pedidos = pedido.consultarPedidoBDD();
    DefaultTableModel modeloTabla = (DefaultTableModel) jTablePedidos.getModel();
    for (Document doc : pedidos) {
        int numeroPedido = doc.getInteger("#");
        String producto = doc.getString("Producto");
        String apellidosCliente = doc.getString("Apellidos");
        double precio = doc.getDouble("Precio"); // Obtener el precio del pedido
        
        // Agregar fila a la tabla con los datos del pedido y el precio
        modeloTabla.addRow(new Object[]{numeroPedido, producto, apellidosCliente, precio});
    }
}
